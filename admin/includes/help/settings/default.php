<?php if (!defined('IPHORM_VERSION')) exit; ?><h4>Default value</h4>
<p>The default value specified will appear already entered in the element when the form is displayed to the user.</p>
<p>You can insert a variable into the default value and it will be replaced with the actual value
when the form is shown to the user, for example you can insert the current date, the current URL or the user's email address.
See the Insert variable dropdown entries for what you can use.</p>
<h4>Clear default value on focus</h4>
<p>Tick this box to make it clear the field when the user first clicks into it.</p>
<h4>Show default value on blur if empty</h4>
<p>Tick this box to make it show the default value again when the user clicks off the field and leaves
it empty.</p>